const selectedLang = localStorage.getItem('AppLanguageCode') || 'en';

const getTranslationByLang = (langCode, translationKey) => {
  return translations[langCode]?.[translationKey] || translations['en'][translationKey] || translationKey;
};

const applyTranslations = (langCode) => {
  document.querySelectorAll("[data-translate]").forEach((el) => {
    const translationKey = el.getAttribute("data-translate");
    el.textContent = getTranslationByLang(langCode, translationKey);
  });
};

const setLanguage = (lang) => {
  localStorage.setItem('AppLanguageCode', lang);
  applyTranslations(lang);
};

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations(selectedLang);
});

